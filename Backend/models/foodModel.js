import mongoose from 'mongoose';
import validator from 'validator';

const foodSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true
    },
    category: {
        type: String,
        required: true,
        trim: true
    },
    price: {
        type: Number,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    image: {
        type: String,
        default: ''
    }
});

const Food = mongoose.model('Food', foodSchema);

export default Food;
